    private void sendBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendBtnActionPerformed
        try {
            Properties properties = new Properties();
            properties.put("mail.smtp.auth", "true");
            properties.put("mail.smtp.starttls.enable", "true");
            properties.put("mail.smtp.host", "smtp.gmail.com");
            properties.put("mail.smtp.port", "587");
            Session session = Session.getInstance(properties, new javax.mail.Authenticator() {
                @Override
                protected PasswordAuthentication getPasswordAuthentication() {
                    return new PasswordAuthentication(userField.getText(), pwdField.getText());
                }
            });
            //multiple recipients
            String[] recipientsList = toField.getText().split(" ");
            InternetAddress[] recipientAddresses = new InternetAddress[recipientsList.length];
            int i = 0;
            for (String e : recipientsList) {
                recipientAddresses[i] = new InternetAddress(e.trim());
                i++;
            }

            String from = userField.getText();
            String subject = subjectField.getText();
            String content = contentField.getText();

            Message message = new MimeMessage(session);
            message.setFrom(new InternetAddress(from));
            message.setRecipients(Message.RecipientType.TO, recipientAddresses);
            message.setSubject(subject);
            message.setSentDate(new Date());

            //attachment
            Multipart multipart = new MimeMultipart();

            //content
            BodyPart bodyPart = new MimeBodyPart();
            bodyPart.setText(content);
            multipart.addBodyPart(bodyPart);

            //files
            if ((lastPressed == browseBtn) && (!attachField.getText().isEmpty())) {
                for (File file : selectedFiles) {
                    DataSource dataSource = new FileDataSource(file);
                    MimeBodyPart mimeBodyPart = new MimeBodyPart();
                    mimeBodyPart.setDataHandler(new DataHandler(dataSource));
                    mimeBodyPart.setFileName(file.getName());
                    multipart.addBodyPart(mimeBodyPart);
                }
            }
            message.setContent(multipart);
            Transport.send(message);
            JOptionPane.showMessageDialog(this, "Message sent!!");

        } catch (MessagingException e) {
            JOptionPane.showMessageDialog(this, e);
            e.printStackTrace();
        }
    }//GEN-LAST:event_sendBtnActionPerformed

